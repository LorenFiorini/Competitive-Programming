void solve () {
	ll x = -1, y = -1;
	ll a, b, c, d;
	cin >> a >> b >> c >> d;

	// algebraically not possible
	if (c < b || d < a) {showp(x,y); return;}

	if (a < b && b <= c) {
		x = b;
	} else if (b < a && a <= d) {
		x = a;
		swap(a,b);
		swap(c,d);
	}

	// work with B < A*K <= D

	if (2 * a * x <= c*d) {
		y = 2 * a;
	}
	showp(x, y);
}

int main () 
{
	ios::sync_with_stdio(false);
	cin.tie(nullptr);
	
	int t; cin >> t; while (t--)
	
	solve();
	
	return 0;
}

