void solve () {
	string num;
	cin >> num;
	int sz = len(num);
	int a = 0, b = 0;
	int ca = 0, cb = 0;

	for (char c: num) {
		int d = c - '0';
		a *= 10; b *= 10;
		a += d / 2; b += d / 2;
		ca += d / 2; cb += d / 2;

		if (d % 2) {
			if (ca > cb) {
				cb++;
				b++;
			} else {
				ca++;
				a++;
			}
		}
	}
	
	
	showp(a, b);
}


